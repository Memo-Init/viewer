// collect-mermaid-errors.mjs
//
// Extracts every ```mermaid code-block from all .md files under .memo/**/revisions/
// and writes them as .mmd files plus an INDEX.md for diagnosis (PRD-022, Memo 011 Kap 3.1).
//
// Diagnose-first: this script does NOT change mermaid versions. It only collects
// reproduction cases so test-mermaid-versions.mjs can later render them across versions.
//
// Usage:
//   node editor/scripts/collect-mermaid-errors.mjs
//   node editor/scripts/collect-mermaid-errors.mjs --memo-root=../.memo --out=editor/tests/manual/mermaid-error-collection/extracted
//
// Output:
//   <out>/INDEX.md                           — table of all extracted diagrams
//   <out>/<memo>-<rev>-<index>.mmd           — one file per diagram

import { readdir, readFile, writeFile, mkdir } from 'node:fs/promises'
import { existsSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'


const parseArgs = ( argv ) => {
    const result = { 'memoRoot': null, 'out': null }

    argv
        .filter( ( arg ) => arg.startsWith( '--' ) )
        .forEach( ( arg ) => {
            const eq = arg.indexOf( '=' )
            const key = eq > -1 ? arg.slice( 2, eq ) : arg.slice( 2 )
            const val = eq > -1 ? arg.slice( eq + 1 ) : 'true'

            if( key === 'memo-root' ) { result.memoRoot = val }
            if( key === 'out' ) { result.out = val }
        } )

    return result
}


const collectMarkdownFiles = async ( { dir } ) => {
    const collected = []

    const walk = async ( current ) => {
        let entries

        try {
            entries = await readdir( current, { 'withFileTypes': true } )
        } catch( e ) {
            return
        }

        await Promise.all(
            entries.map( async ( entry ) => {
                const full = path.join( current, entry.name )

                if( entry.isDirectory() ) {
                    if( entry.name === 'node_modules' || entry.name.startsWith( '.git' ) ) { return }
                    await walk( full )

                    return
                }

                if( entry.isFile() && entry.name.endsWith( '.md' ) ) {
                    if( current.includes( `${path.sep}revisions${path.sep}` ) || current.endsWith( `${path.sep}revisions` ) ) {
                        collected.push( full )
                    }
                }
            } )
        )
    }

    await walk( dir )

    return collected
}


const extractMermaidBlocks = ( { content, sourcePath, memoRoot } ) => {
    const pattern = /```mermaid\n([\s\S]*?)\n```/g
    const blocks = []
    const lines = content.split( '\n' )

    let match
    let index = 0

    while( ( match = pattern.exec( content ) ) !== null ) {
        index = index + 1

        const code = match[ 1 ]
        const offset = match.index
        const linesBefore = content.slice( 0, offset ).split( '\n' )
        const startLine = linesBefore.length

        const headingMatch = lines
            .slice( 0, startLine )
            .reverse()
            .find( ( line ) => line.startsWith( '#' ) )

        const heading = headingMatch ? headingMatch.replace( /^#+\s*/, '' ).trim() : '(no heading)'

        const relSource = path.relative( memoRoot, sourcePath )
        const parts = relSource.split( path.sep )
        const memoSegment = parts[ 1 ] || parts[ 0 ] || 'unknown'
        const fileBase = path.basename( sourcePath, '.md' )

        blocks.push( {
            'index':      index,
            'sourcePath': sourcePath,
            'relSource':  relSource,
            'memo':       memoSegment,
            'rev':        fileBase,
            'heading':    heading,
            'startLine':  startLine,
            'code':       code
        } )
    }

    return blocks
}


const writeExtractedFile = async ( { block, outDir } ) => {
    const safeMemo = block.memo.replace( /[^a-z0-9-]/gi, '_' )
    const safeRev = block.rev.replace( /[^a-z0-9-]/gi, '_' )
    const filename = `${safeMemo}-${safeRev}-${String( block.index ).padStart( 2, '0' )}.mmd`
    const fullPath = path.join( outDir, filename )

    const header = [
        `%% Source: ${block.relSource}`,
        `%% Heading: ${block.heading}`,
        `%% Line: ${block.startLine}`,
        ''
    ].join( '\n' )

    await writeFile( fullPath, header + block.code + '\n', 'utf8' )

    return { 'filename': filename, 'fullPath': fullPath }
}


const writeIndex = async ( { rows, outDir } ) => {
    const indexPath = path.join( outDir, 'INDEX.md' )

    const lines = [
        '# Mermaid Error Collection Index',
        '',
        '> Generated by `editor/scripts/collect-mermaid-errors.mjs` (PRD-022).',
        '> Diagnose-first: this index lists all mermaid code blocks extracted from `.memo/**/revisions/` for cross-version testing.',
        '',
        `Total diagrams collected: ${rows.length}`,
        '',
        '| ID | Memo | Revision | Heading | Source | Line | File |',
        '|----|------|----------|---------|--------|------|------|'
    ]

    rows.forEach( ( row ) => {
        const id = `${row.memo}-${row.rev}-${String( row.index ).padStart( 2, '0' )}`
        const heading = row.heading.replace( /\|/g, '\\|' )

        lines.push(
            `| \`${id}\` | ${row.memo} | ${row.rev} | ${heading} | \`${row.relSource}\` | ${row.startLine} | \`${row.filename}\` |`
        )
    } )

    lines.push( '' )

    await writeFile( indexPath, lines.join( '\n' ), 'utf8' )

    return { 'indexPath': indexPath }
}


const collect = async ( { memoRoot, outDir } ) => {
    if( !existsSync( memoRoot ) ) {
        return { 'status': 'no-memo-root', 'memoRoot': memoRoot, 'collected': 0 }
    }

    await mkdir( outDir, { 'recursive': true } )

    const files = await collectMarkdownFiles( { 'dir': memoRoot } )
    const rows = []

    await files.reduce( async ( prev, file ) => {
        await prev

        const content = await readFile( file, 'utf8' )
        const blocks = extractMermaidBlocks( { 'content': content, 'sourcePath': file, 'memoRoot': memoRoot } )

        await blocks.reduce( async ( inner, block ) => {
            await inner

            const written = await writeExtractedFile( { 'block': block, 'outDir': outDir } )

            rows.push( { ...block, 'filename': written.filename } )
        }, Promise.resolve() )
    }, Promise.resolve() )

    const indexResult = await writeIndex( { 'rows': rows, 'outDir': outDir } )

    return { 'status': 'done', 'memoRoot': memoRoot, 'collected': rows.length, 'indexPath': indexResult.indexPath, 'outDir': outDir }
}


const main = async () => {
    const here = path.dirname( fileURLToPath( import.meta.url ) )
    const editorRoot = path.resolve( here, '..' )
    const memoToolkitRoot = path.resolve( editorRoot, '..' )
    const workspaceRoot = path.resolve( memoToolkitRoot, '..', '..' )

    const args = parseArgs( process.argv.slice( 2 ) )
    const memoRoot = args.memoRoot ? path.resolve( args.memoRoot ) : path.join( workspaceRoot, '.memo' )
    const outDir = args.out
        ? path.resolve( args.out )
        : path.join( editorRoot, 'tests', 'manual', 'mermaid-error-collection', 'extracted' )

    const result = await collect( { 'memoRoot': memoRoot, 'outDir': outDir } )

    if( result.status === 'no-memo-root' ) {
        console.log( `[COLLECT] Memo-Root nicht gefunden: ${result.memoRoot}` )
        console.log( '[COLLECT] Nutze --memo-root=<pfad>, um den Pfad zu setzen.' )

        return
    }

    console.log( `[COLLECT] Diagramme extrahiert: ${result.collected}` )
    console.log( `[COLLECT] Output-Ordner: ${result.outDir}` )
    console.log( `[COLLECT] Index: ${result.indexPath}` )
}


main().catch( ( e ) => {
    console.error( '[COLLECT] Fehler:', e )
    process.exit( 1 )
} )


export { parseArgs, extractMermaidBlocks, collect }
